    <h2 id="ih2"><U>ROLE OF RESUME IN EVERYDAY LIFE</U></h2>


    <h3 style="margin-top:30px"><strong>Why Is a Resume Important?</strong></h4>
      <p class="p1" style="width:80%; margin:auto; font-size:18px;  margin-top: 10px">
        A resume is an important tool for your job search because it offers a page or two where you can display your top skills and qualities. However, a resume is much more than that. Resumes help employers make hiring decisions and help you get
        your
        first interview. That's why it matters how you structure your resume and what information you decide to include. In this article, you'll learn why a resume is important and get actionable resume tips that may help you achieve your next career
        move.
      </p>


      <h3 style="margin-top:30px"> <strong> Why do employers care about your resume?</strong> </h3>
      <p class="p2" style="width:80%; margin:auto; font-size:18px; margin-top: 10px">
        Resumes, usually accompanied by customized cover letters, get sent to employers to determine your eligibility and qualifications for a job. Employers use resumes to get a deeper understanding of candidate skills, strengths and experience.
        Your
        resume should reflect achievements, awards, education, experience and any other outstanding accomplishments that align with your career path and goals. Your resume is your first point of contact with the employer and sets the tone for
        subsequent steps such as first interview, second interview, pre-screening and on-boarding.
      </p>


      <h3 style="margin-top:30px"> <strong>Why do you need a resume?</strong> </h3>
      <p class="p3" style="width:80%; margin:auto; font-size:18px;  margin-top: 10px">
        In today's competitive market of professional jobs, a resume is usually a base requirement for moving forward in the interview process. A good resume will immediately display to employers why the candidate is a good fit. Here are some reasons
        why you need a resume to get your next job. An effective resume:
        <br>
      <ul class="ul1" style="font-size:18px">
        <li>Outlines your relevant skills and experience</li>
        <li>Displays the benefits you offer employers</li>
        <li>Grabs the attention of employers</li>
        <li>Matches you to the position</li>
        <li>Can lead to an interview</li>
      </ul>
      </p>

